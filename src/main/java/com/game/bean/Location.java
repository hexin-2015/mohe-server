package com.game.bean;

public class Location {
	
	public float top;
	public float left;
	public int rotation;
	public int width;
	public int height;
	@Override
	public String toString() {
		return "Location [top=" + top + ", left=" + left + ", rotation=" + rotation + ", width=" + width + ", height="
				+ height + "]";
	}
	
}
